// ============================================================
// 帧缓冲（仅在 SSD1306_USE_FRAMEBUFFER 时分配）
// ============================================================

#ifdef SSD1306_USE_FRAMEBUFFER
uint8_t oled_buf[SSD1306_BUF_LEN];
#endif

// ============================================================
// 内部辅助函数
// ============================================================

static void ssd1306_write_cmd(uint8_t cmd)
{
    uint8_t buf[2] = {0x00, cmd};
    i2c_write_blocking(SSD1306_I2C_PORT, SSD1306_I2C_ADDR, buf, 2, false);
}

#ifndef SSD1306_USE_FRAMEBUFFER
static void ssd1306_set_pos(uint8_t page, uint8_t col)
{
    uint8_t buf[4] = {
        0x00,
        0xB0 | (page & 0x07),
        0x00 | (col & 0x0F),
        0x10 | ((col >> 4) & 0x0F)
    };
    i2c_write_blocking(SSD1306_I2C_PORT, SSD1306_I2C_ADDR, buf, 4, false);
}
#endif

// ============================================================
// 公开 API
// ============================================================

void ssd1306_init(void)
{
    i2c_init(SSD1306_I2C_PORT, SSD1306_I2C_BAUD);
    gpio_set_function(SSD1306_SDA_PIN, GPIO_FUNC_I2C);
    gpio_set_function(SSD1306_SCL_PIN, GPIO_FUNC_I2C);
    gpio_pull_up(SSD1306_SDA_PIN);
    gpio_pull_up(SSD1306_SCL_PIN);

    sleep_ms(100);

    static const uint8_t init_cmds[] = {
        SSD1306_SET_DISP,
        SSD1306_SET_MEM_MODE,       0x00,
        SSD1306_SET_DISP_START_LINE,
        SSD1306_SET_SEG_REMAP | 0x01,
        SSD1306_SET_COM_OUT_DIR | 0x08,
        SSD1306_SET_COM_PIN_CFG,     0x12,
        SSD1306_SET_CONTRAST,        0xFF,
        SSD1306_SET_ENTIRE_ON,
        SSD1306_SET_NORM_DISP,
        SSD1306_SET_DISP_CLK_DIV,    0x80,
        SSD1306_SET_CHARGE_PUMP,     0x14,
        SSD1306_SET_DISP_OFFSET,     0x00,
        SSD1306_SET_PRECHARGE,       0xF1,
        SSD1306_SET_VCOM_DESEL,      0x30,
        SSD1306_SET_MUX_RATIO,       SSD1306_HEIGHT - 1,
        SSD1306_SET_SCROLL,
        SSD1306_SET_DISP | 0x01,
    };

    for (uint8_t i = 0; i < sizeof(init_cmds); i++)
        ssd1306_write_cmd(init_cmds[i]);

    ssd1306_clear();
#ifdef SSD1306_USE_FRAMEBUFFER
    ssd1306_render();
#endif
}

void ssd1306_clear(void)
{
#ifdef SSD1306_USE_FRAMEBUFFER
    memset(oled_buf, 0, SSD1306_BUF_LEN);
#else
    for (uint8_t page = 0; page < SSD1306_PAGES; page++) {
        ssd1306_set_pos(page, 0);
        uint8_t buf[129];
        buf[0] = 0x40;
        memset(buf + 1, 0, 128);
        i2c_write_blocking(SSD1306_I2C_PORT, SSD1306_I2C_ADDR, buf, 129, false);
    }
#endif
}

void ssd1306_draw_string(uint8_t page, uint8_t col, const char *str)
{
    uint8_t max_chars = (SSD1306_WIDTH - col) / 6;

#ifdef SSD1306_USE_FRAMEBUFFER
    int fb_idx = page * SSD1306_WIDTH + col;
    for (uint8_t i = 0; i < max_chars; i++) {
        char c = *str ? *str : ' ';
        if (*str) str++;
        if (c < 0x20 || c > 0x7E) c = ' ';
        memcpy(oled_buf + fb_idx, font5x8[c - 0x20], 5);
        fb_idx += 5;
        oled_buf[fb_idx++] = 0x00;
    }
#else
    ssd1306_set_pos(page, col);
    uint8_t buf[129];
    buf[0] = 0x40;
    size_t len = 1;

    for (uint8_t i = 0; i < max_chars; i++) {
        char c = *str ? *str : ' ';
        if (*str) str++;
        if (c < 0x20 || c > 0x7E) c = ' ';
        memcpy(buf + len, font5x8[c - 0x20], 5);
        len += 5;
        buf[len++] = 0x00;
    }

    if (len > 1) {
        i2c_write_blocking(SSD1306_I2C_PORT, SSD1306_I2C_ADDR, buf, len, false);
    }
#endif
}

void ssd1306_draw_string_centered(uint8_t page, const char *str)
{
    int len = strlen(str);
    int col = (SSD1306_WIDTH - len * 6) / 2;
    if (col < 0) col = 0;
    ssd1306_draw_string(page, (uint8_t)col, str);
}

void ssd1306_clear_page(uint8_t page)
{
    ssd1306_draw_string(page, 0, "");
}

void ssd1306_clear_page_range(uint8_t start, uint8_t end)
{
    for (uint8_t p = start; p <= end; p++)
        ssd1306_clear_page(p);
}

#ifdef SSD1306_USE_FRAMEBUFFER
void ssd1306_render(void)
{
    static uint8_t render_buf[SSD1306_BUF_LEN + 1];
    uint8_t cmds[] = {
        SSD1306_SET_COL_ADDR, 0, SSD1306_WIDTH - 1,
        SSD1306_SET_PAGE_ADDR, 0, SSD1306_PAGES - 1
    };

    for (uint8_t i = 0; i < sizeof(cmds); i++)
        ssd1306_write_cmd(cmds[i]);

    render_buf[0] = 0x40;
    memcpy(render_buf + 1, oled_buf, SSD1306_BUF_LEN);
    i2c_write_blocking(SSD1306_I2C_PORT, SSD1306_I2C_ADDR,
                       render_buf, SSD1306_BUF_LEN + 1, false);
}
#endif
